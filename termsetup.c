#include "minishell.h"

void    signal_ctlc(int sig)
{
        if (sig == SIGINT)
        {
                write(1, "\n", 1);
                rl_replace_line("", 0);
                rl_on_new_line();
                rl_redisplay();
        }
}

void    signal_ctlc_heredoc(int sig)
{
        if (sig == SIGINT)
        {
                close(STDIN_FILENO);
                write(STDERR_FILENO, "\n", 1);
        }
}
